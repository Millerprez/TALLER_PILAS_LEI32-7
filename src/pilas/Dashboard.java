    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        Node aux = SProfesores.getTope();
        String eleccion = cbNAsignaturas.getSelectedItem().toString();
        int cantidadProfesores = 0, cantidadAsignatura = 0;

        if (eleccion.equalsIgnoreCase("Seleccione")) {
            JOptionPane.showMessageDialog(null, "Debe elegir una asignatura");
        } else if (!SProfesores.isEmpty()) {
            while (aux != null) {
                Profesor p = (Profesor) aux.getDato();
                if (p.getAsignatura().equalsIgnoreCase(eleccion)) {
                    cantidadAsignatura++;
                }
                cantidadProfesores++;
                aux = aux.getNext();
            }

            JOptionPane.showMessageDialog(null, "El porcentaje de profesores que dictan "
                    + eleccion + " es del " + (cantidadAsignatura * 100 / cantidadProfesores) + "%");

        } else {
            JOptionPane.showMessageDialog(null, "Debe ingresar al menos un profesor");
        }

    }//GEN-LAST:event_jButton4ActionPerformed

