    public boolean verificarCedula(String cedula) {
        return cedula.length() < 6 || cedula.length() > 10;
    }

    public boolean verificarCodigoAsignatura(String code) {
        return code.length() < 5 || code.length() > 9;
    }

    public boolean verificarProfesor(String cedula, String nombres, String apellidos,
            String asignatura) {
        return (verificarCedula(cedula)
                || "Seleccione".equalsIgnoreCase(nombres)
                || "Seleccione".equalsIgnoreCase(apellidos)
                || "Seleccione".equalsIgnoreCase(asignatura));
    }

    public void mostrarProfesores() {
        Node aux = SProfesores.getTope();
        String msj = "";
        //Cargar las asignaturas en el combobox
        if (!SProfesores.isEmpty()) {
            while (aux != null) {
                Profesor p = (Profesor) aux.getDato();
                msj += p.getNombres() + " - " + p.getAsignatura() + "\n";
                aux = aux.getNext();
            }
            JOptionPane.showMessageDialog(null, msj);
        } else {
            JOptionPane.showMessageDialog(null, "No hay Profesores ingresados");
        }
    }

    public boolean verificarAsignatura(String codigo, String numeroGrupo, String nombre,
            String duracion, String creditos) {
        return (verificarCodigoAsignatura(codigo)
                || "Seleccione".equalsIgnoreCase(numeroGrupo)
                || "".equalsIgnoreCase(nombre)
                || "Seleccione".equalsIgnoreCase(duracion)
                || "Seleccione".equalsIgnoreCase(creditos));
    }

